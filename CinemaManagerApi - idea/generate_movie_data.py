# 5. 生成 session_seats 的 JSON
# ============================================================
def generate_session_seats(rows, cols):
    """0=可选, 1=无座位, 随机一些为1"""
    import random
    random.seed(42)
    seats = {}
    for r in range(1, rows + 1):
        row_seats = []
        for c in range(cols):
            row_seats.append(0 if random.random() > 0.05 else 1)
        seats[str(r)] = row_seats
    return json.dumps(seats, ensure_ascii=False)

