# 计算散场时间
def calc_end_time(start_time, movie_length):
    h, m = map(int, start_time.split(':'))
    total_min = h * 60 + m + movie_length
    end_h = total_min // 60
    end_m = total_min % 60
    return f"{end_h:02d}:{end_m:02d}"

# 计算截止时间 (开始前30分钟)，返回完整 datetime
def calc_deadline(play_date, start_time):
    h, m = map(int, start_time.split(':'))
    total_min = h * 60 + m - 30
    if total_min < 0:
        total_min = 0
    dh = total_min // 60
    dm = total_min % 60
    return f"{play_date} {dh:02d}:{dm:02d}:00"


